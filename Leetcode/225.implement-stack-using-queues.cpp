// @lc code=start
class MyStack
{
public:
    MyStack()
    {
    }
    queue<int> q;
    void push(int x)
    {
        int cnt = q.size();
        q.push(x);
        while (cnt--)
        {
            q.push(q.front());
            q.pop();
        }
    }

    int pop()
    {
        int x = q.front();
        q.pop();
        return x;
    }

    int top()
    {
        return q.front();
    }

    bool empty()
    {
        if (q.empty())
            return true;
        else
            return false;
    }
};

